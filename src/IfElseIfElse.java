public class IfElseIfElse {
    public static void main(String[] args) {
        int age = 2;
        if (age >= 21) {
            System.out.println("You're old enough to drink");
        } else if (age < 21) {
            System.out.println("You're not old enough to drink");
        } else {
            System.out.println("You're probably too old or too young.");
        }
    }
}
